#include <unistd.h>

void	print_char(char c)
{
	write(1, &c, 1);
}

void	ft_is_negative(int n)
{
	if (n < 0)
	{
		print_char('N');
	}
	else if (n >= 0)
	{
		print_char('P');
	}
}
